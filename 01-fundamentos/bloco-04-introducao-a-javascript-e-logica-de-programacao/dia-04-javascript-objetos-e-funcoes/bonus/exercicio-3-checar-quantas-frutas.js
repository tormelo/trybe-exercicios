function createFruitsObjectFromArray(array) {
  let fruits = {};

  for (const fruit of array) {
    if (fruits[fruit] === undefined) {
      fruits[fruit] = 1;
    } else {
      fruits[fruit] += 1;
    }
  }

  return fruits;
}

function logNumberOfFruits(object) {
  let summaries = [];
  let summary;

  for (const fruit in object) {
    summary = '';
    summary += `${object[fruit]} ${fruit}`;
    if (object[fruit] > 1) {
      summary += 's';
    }
    summaries.push(summary);
  }

  console.log(`Sua cesta possui: ${summaries.join(', ')}.`);
}

